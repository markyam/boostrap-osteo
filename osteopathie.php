					</ul>
				</p>
				<strong style="color:red;">STRUCTURE EQUILIBREE + MOBILITE CORRECTE = FONCTION OPTIMALE</strong>
				<header>
					<span class="byline"><b>Les différentes techniques</b></span>
				</header>
				<p>L’osteopathe est capable d’utiliser plusieurs techniques qui vont lui permettre d’adapter un traitement à chaque patient mais aussi répondre le plus efficacement possible aux différents types de douleurs.</p>
				<header>
					<span class="byline" style="color: #80BA8E;">Technique structurelle ( « thrust » )</span>
				</header>
				<p>Le but est de redonner de la mobilité à une articulation de façon <b>directe</b>. <b>Technique rapide</b> éxecutée à travers un mouvement <b>bref</b> et <b>précis</b>. Il résulte souvent un bruit de craquement.</p>
				<header>
					<span class="byline" style="color: #80BA8E;">Technique fonctionnelle</span>
				</header>
				<p>Le but est de libérer les blocages d’une articulation en prenant en compte tous ses paramètres de mobilité ( flexion / extension / rotation / inclinaison / compression / traction ) et en y ajoutant un <b>paramètre respiratoire</b> ( inspiration ou expiration ) qui aide à la <b>correction</b>.</p>
				<header>
					<span class="byline" style="color: #80BA8E;">Technique musculaire</span>
				</header>
				<p>Le but est de travailler essentiellement sur la <b>structure musculaire</b> en étirements, contraction isométrique ( contraction / relâchement ) ou avec des <b>massages profonds</b> ou <b>point de pression</b> (trigger point ).</p>
				<header>
					<span class="byline" style="color: #80BA8E;">Technique viscerale</span>
				</header>
				<p>Le but est de redonner de la <b>mobilité</b> aux organes en travaillant sur les différentes attaches (ligaments et enveloppes tissulaires) afin d’optimiser leurs fonctionnalités. On peut également travailler en pompage afin de stimuler certains organes en effectuant nos manipulations avec la <b>respiration</b> du patient.</p>
				<header>
					<span class="byline" style="color: #80BA8E;">Technique crânienne</span>
				</header>
				<p>Les os du cranes possèdent une micro-mobilité (nommée Mouvement Respiratoire Primaire ) en corrélation avec la fluctuation du Liquide céphalo rachidien et de la respiration thoracique. Diffèrentes pertes de mobilité peuvent apparaître au cours de la vie (compression / blocage ) à ce niveau. Les techniques sont <b>douces</b> et réalisées par <b>pressions légères</b> de façon direct ou indirect et peuvent utiliser les mouvements volontaires du patient ( de la tête, des yeux, de la langue ou des pieds ) ainsi que la respiration. Cette approche est souvent appelée « crânio-sacrée » car le crâne fonctionne en étroite relation avec le sacrum.</p>
